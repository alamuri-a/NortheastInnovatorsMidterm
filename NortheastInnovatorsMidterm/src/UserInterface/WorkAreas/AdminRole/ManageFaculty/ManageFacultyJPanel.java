    // EXTRA METHODS
    public void refreshTable() {
        // Update table with current values from FacultyDirectory
        
        // Clear table
        DefaultTableModel model =  (DefaultTableModel) tblFaculty.getModel();
        model.setRowCount(0);
        
        // Add each entry to table
        FacultyDirectory fd = business.getFacultyDirectory();
        for (FacultyProfile fp : fd.getFacultyList()) {
            Object[] row = new Object[1];
            row[0] = fp.getPerson().getPersonId();
            
            model.addRow(row);
        }
    }
