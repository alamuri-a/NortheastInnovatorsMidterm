    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // Ensure person is selected then load person management pane

        int selectedRow = tblFaculty.getSelectedRow();
        if (selectedRow < 0 || selectedRow > tblFaculty.getRowCount()) {
            JOptionPane.showMessageDialog(null, "Please select a Faculty from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Find the corresponding Faculty profile and corresponding null check
        FacultyDirectory fd = business.getFacultyDirectory();
        FacultyProfile selectedFaculty =  fd.findEmployee((String) tblFaculty.getValueAt(selectedRow, 0));
        if (selectedFaculty == null) {
            JOptionPane.showMessageDialog(null, "Profile could not be found for selected Faculty.",  "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        UpdateFacultyJPanel usp = new UpdateFacultyJPanel(business, user, selectedFaculty, CardSequencePanel);
        CardSequencePanel.add(usp);
        ((CardLayout) CardSequencePanel.getLayout()).next(CardSequencePanel);
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Remove selected account from Faculty directory + update table
        
        // Notify if no user is selected
        int selectedRow = tblFaculty.getSelectedRow();
        if (selectedRow < 0 || selectedRow > tblFaculty.getRowCount()) {
            JOptionPane.showMessageDialog(null, "Please select a Faculty from the table.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Find the corresponding Faculty profile and corresponding null check
        FacultyDirectory fd = business.getFacultyDirectory();
        FacultyProfile selectedFaculty =  fd.findEmployee((String) tblFaculty.getValueAt(selectedRow, 0));
        if (selectedFaculty == null) {
            JOptionPane.showMessageDialog(null, "Profile could not be found for selected Faculty.",  "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Display confirmation dialog for deletion
        int dialogResult = JOptionPane.showConfirmDialog(null,"Are you sure you want to delete the selected Faculty profile?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            fd.deleteFacultyProfile(selectedFaculty);
            refreshTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


