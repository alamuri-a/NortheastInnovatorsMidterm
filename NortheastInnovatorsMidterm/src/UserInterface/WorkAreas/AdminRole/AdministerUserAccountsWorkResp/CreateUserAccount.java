    private void cmbRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbRoleActionPerformed
        // Display NUID based on currently selected role
        
        String selectedOption = (String) cmbRole.getSelectedItem();
        
        if (selectedOption.equals("Student")) {
            lblNUID.setVisible(true);
            txtNUID.setVisible(true);
        } else {
            lblNUID.setVisible(false);
            txtNUID.setVisible(false);
        }
    }//GEN-LAST:event_cmbRoleActionPerformed


