    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // Update fields and notify user, then return to user account table page

        String person = txtID.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        Integer nuid = null;

        // Blank checks
        if (person.isBlank() || username.isBlank() || password.isBlank()) {
            JOptionPane.showMessageDialog(null, "Person ID/Username/Password cannot be blank.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String role = (String) cmbRole.getSelectedItem();
        if (role.equals("Student")) {
            // NUID check for student account
            try {
                nuid = Integer.valueOf(txtNUID.getText());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "NUID must be an integer.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        // Check if person has a profile of selected type and notify if invalid Person ID
        Profile p = null;
        switch (role) {
            case "Student":
                p = business.getStudentDirectory().findStudent(person);
                break;
            case "Faculty":
                p = business.getFacultyDirectory().findEmployee(person);
                break;
            case "Admin":
                p = business.getEmployeeDirectory().findEmployee(person);
                break;
        }
        if (p == null) {
            JOptionPane.showMessageDialog(null, "Person ID does not match a user with specified profile type", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Create UserAccount/StudentAccount
        if (role.equals("Student")) {
            business.getUserAccountDirectory().newStudentAccount((StudentProfile)p, username, password, nuid);
        } else {
            business.getUserAccountDirectory().newUserAccount(p, username, password);
        }
        JOptionPane.showMessageDialog(null, "User account successfully created!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Update table on previous page and return
        CardSequencePanel.remove(this);

        Component[] panelStack = CardSequencePanel.getComponents();
        JPanel lastPanel = (JPanel) panelStack[panelStack.length - 1];
        ManageUserAccountsJPanel manageUserAccountsJPanel = (ManageUserAccountsJPanel) lastPanel;
        manageUserAccountsJPanel.refreshTable(); // Update table

        CardLayout layout = (CardLayout) CardSequencePanel.getLayout();
        layout.previous(CardSequencePanel);
    }//GEN-LAST:event_btnCreateActionPerformed

